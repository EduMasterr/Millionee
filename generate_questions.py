idx = 60000

def add(level, q, ans, pool):
    global idx
    candidates = [x for x in pool if x != ans]
    if len(candidates) < 3: return
    dists = random.sample(candidates, 3)
    opts = dists + [ans]; random.shuffle(opts)
    db[str(level)].append({'id': f'v2_{idx}', 'question': q, 'options': opts, 'answer': ans})
    idx += 1

